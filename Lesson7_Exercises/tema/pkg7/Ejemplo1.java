    private void crearArray(int tamanio){
            altura = new int[tamanio];//dentro de los corchetes podemos poner 100 por ejemplo pero en este caso es variable porque l ointroduce el usuario.
            
            System.out.println("Array creado de tamanio " + altura.length);
        
            //Arrays.fill(altura, -1);
            //mostrarArray();
        
            rellenarArray();
            mostrarArray();
    }
    //muestra el contenido del array
    private void mostrarArray(){
        for(int i=0; i< altura.length; i++){
            System.out.println("Posicion: " + i + " " + altura[i]);
        }
        
    }
    private void rellenarArray(){
        //rellena el array con valores aleatorios entre 10 y 300
        Random r = new Random();
        for(int j=0;j <altura.length;j++){
            altura[j]=r.nextInt(10, 300);
        }
    }
    private void alturaMedia(){
        int suma =0;
        int cont=0;
        for(int i=0; i<altura.length;i++){
           if(altura[i] >=0){
               cont ++;
               suma += altura[i];
           }
        }
        System.out.println("Altura media: " + (double)suma/cont);
    }
    private void alturaMinMax(){
       int alturaMin=Integer.MAX_VALUE;
       int alturaMax=Integer.MIN_VALUE;
       for(int i=0; i < altura.length;i++){
           if(altura[i]<alturaMin){
               alturaMin=altura[i];
           }
           if(altura[i]<alturaMax){
               alturaMax=altura[i];
           }
       }
        System.out.printf("Altura minima %d %n", alturaMin);
        System.out.printf("Altura minima %d %n", alturaMax);
               
    }
