    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ConexionBd conexion = new ConexionBd(); 
    LibrosDao librosDao = new LibrosDao(conexion);
    List<Libros> libros = librosDao.obtenerTodos();  // Obtenemos la lista de libros

    // Limpiamos la tabla antes de mostrar los nuevos resultados
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0);  // Limpia las filas existentes

    // Ahora mostramos los resultados en la tabla
    for (Libros libro : libros) {
        modelo.addRow(new Object[]{
            libro.getId(),
            libro.getTitulo(),
            libro.getAutor(),
            libro.getGenero(),
            libro.getAnio_publicacion()
        });
    }

    // También podrías cerrar la conexión después de realizar la consulta, si lo consideras necesario.
    conexion.desconectar();     
        
    }//GEN-LAST:event_jButton1ActionPerformed

