    private void setFrame(){
        ConexionBd conexion = new ConexionBd();
        librosDao = new LibrosDao(conexion);
        modeloTabla = (DefaultTableModel) jTable1.getModel();
        modeloTabla.setRowCount(0); // limpia por si acaso
    }
    private void buscarPorAutor() {
    String autor = jTextField1.getText().trim(); // usa el nombre real del campo
    if (autor.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Introduce un autor");
        return;
    }
    List<Libros> libros = librosDao.buscarPorAutor(autor);
    mostrarEnTabla(libros);
    }
    private void buscarPorGenero(){
        String genero = jTextField1.getText().trim();
        if(genero.isEmpty()){
            JOptionPane.showMessageDialog(this, "Introduce un genero");
            return;
        }
    }
    private void mostrarEnTabla(List<Libros> libros) {
        modeloTabla.setColumnCount(0);
        for(Libros l: libros){
            modeloTabla.addRow(new Object[]{
                 l.getId(), l.getTitulo(), l.getAutor(), l.getGenero(), l.getAnio_publicacion()
            });
        }
    }

