    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.grupoOpciones.getSelection()==null){
            JOptionPane.showMessageDialog(this, "Debe elegir una opcion", "Votar",
                    JOptionPane.WARNING_MESSAGE);
            
        }else{
            Respuesta respuesta = new Respuesta(0,0,0);
            if(this.jRadioButton1.isSelected()){
                respuesta.setY(1);
                
            }else if(this.jRadioButton2.isSelected()){
                respuesta.setN(1);
            }else if(this.jRadioButton3.isSelected()){
                respuesta.setNs(1);
            }
            if(respuestaDao.insertRespuesta(respuesta)){
                //voto guardado correctamente
                JOptionPane.showMessageDialog(this, "Gracias, has votado", "Votar",
                        JOptionPane.INFORMATION_MESSAGE);

            }else{
                //error al guardar voto
                JOptionPane.showMessageDialog(this, "Lo siento no has podido votar", "Votar",
                        JOptionPane.ERROR_MESSAGE);

            }
        }
        resetOpciones();
    }//GEN-LAST:event_jButton1ActionPerformed

